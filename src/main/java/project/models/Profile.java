package Pj.models;

public abstract class Profile {
	public abstract String getFirstName();
	public abstract void setFirstName(String firstName);
	public abstract String getLastName();
	public abstract void setLastName(String lastName);
	public abstract String getuName();
	public abstract void setuName(String uName);
	public abstract String getPasswd();
	public abstract void setPasswd(String passwd);
	public abstract String getEmail();
	public abstract void setEmail(String email);
	public abstract String getPhoneNum();
	public abstract void setPhoneNum(String phoneNum);
	
}
